    /**
     * 判断是否全9， 存在数组扩展+1； 如果没有进位直接退出
     * @param digits
     * @return
     */
    public int[] plusOne(int[] digits) {
        int length = digits.length;
        int count = 0;
        while (count < length && digits[count] == 9) {
            count++;
        }
        if (count == length) {
            int[] result = new int[length + 1];
            result[0] = 1;
            return result;
        } else {
            int next = 1;
            for(count = length-1; count >= 0 && next > 0; count--){
                int r = digits[count]+next;
                digits[count] = r%10;
                next = r/10;
            }
            return digits;
        }
    }
